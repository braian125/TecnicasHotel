package hotel;

/**
 *
 * @author Brian Vanegas
 */
public class Person{

    int age;
    String fname;
    String city;

    public Person(int age, String fname, String city) {
        this.age = age;
        this.fname = fname;
        this.city = city;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    @Override
    public String toString() {
        return this.age + " | " + this.fname + " | " + this.city ;
    }
}
