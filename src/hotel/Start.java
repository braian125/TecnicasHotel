    public boolean validarPersona(String identificacion) {
        int j = 0;
        boolean found = false;
        do {
            if (personas.get(j).getIdentificacion().trim().equals(identificacion)) {
                found = true;
            }
            j++;
        } while (found == false && personas.size() > j);
        
        return found;
    }
    
